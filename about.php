Работники автовокзала стремятся обеспечить для пассажиров максимально комфортабельные условия комплексного обслуживания.<br>
<br>
К услугам пассажиров организованы камера хранения багажа, работают четыре кафе, киоски Роспечати, парикмахерская, комната матери и ребенка, медпункт, аптека и другие необходимые сервисные услуги.<br>
<br>
