                       <p>Автовокзал работает с 05-40 до 21-45. Посадка на ночные рейсы осуществляется диспетчером, который работает круглосуточно. <br>
<br>
Автостанция на привокзальной площади в г. Барнауле была открыта в 1939 году. <br>
23.09.1983 года было принято в эксплуатацию существующее  здание автовокзала. <br>
С 01.01.1984 года предприятие находится на самостоятельном балансе с брендом  «Барнаульский автовокзал». <br>
Сегодня автовокзал города Барнаула представляет собой современное, красивое здание, располагающее комплексом помещений для обслуживания пассажиров, автотранспортными стоянками, перроном посадки и платформой высадки пассажиров, площадкой для размещения автобусов и стоянки между рейсами. <br>
